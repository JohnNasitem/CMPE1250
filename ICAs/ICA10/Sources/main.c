void main(void)
{
  //Any main local variables must be declared here
  int counter = 0;
  // main entry point
  _DISABLE_COP();
  //EnableInterrupts;
  
/********************************************************************/
  // one-time initializations
/********************************************************************/
SWL_Init();
Clock_EnableOutput(ClockOutDiv2);
RTI_Init();
Segs_Init();

/********************************************************************/
  // main program loop
/********************************************************************/

  for (;;)
  {
    RTI_Delay_ms(100);
    Segs_Clear();
    Segs_Normal(3, counter++, Segs_DP_OFF);
    if (counter == 16) counter = 0;
  }                   
}

