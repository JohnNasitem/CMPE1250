unsigned long delay;                      //delays the LED toggle

/********************************************************************/
// Constants
/********************************************************************/

/********************************************************************/
// Main Entry
/********************************************************************/
void main(void)
{
  //Any main local variables must be declared here

  // main entry point
  _DISABLE_COP();
  //EnableInterrupts;
  
/********************************************************************/
  // one-time initializations
/********************************************************************/
  PT1AD1 &= 0x1F;
  DDR1AD1 = 0xE0;
  ATD1DIEN1 |= 0x1F;

/********************************************************************/
  // main program loop
/********************************************************************/

  for (;;)
  {
      //Toggle LED
      PT1AD1 ^= 0b10000000;
      //Blocking delay
      for (delay = 0; delay < 1333; delay++) { }
      //Reset delay
      delay = 0;
  }                   
}

